num = int(input("enter any number from 1 to 7 to find corresponding week day"))
if(num==1):
	print("It's Monday")
elif(num==2):
	print("It's Tuesday")
elif(num==3):
	print("It's Wednesday")
elif(num==4):
	print("It's Thursday")
elif(num==5):
	print("It's Friday")
elif(num==6):
	print("It's Saturday")
else:
	print("It's Sunday")
